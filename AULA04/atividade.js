let dia = 0;

switch (dia) {
    case 1:
        console.log("Hoje é domingo.");
        break;
    case 2:
        console.log("Hoje é segunda.");
        break;
    case 3:
        console.log("Hoje é terça.");
        break;
    case 4:
        console.log("Hoje é quarta.");
        break;
    case 5:
        console.log("Hoje é quinta.");
        break;
    case 6:
        console.log("Hoje é sexta.");
        break;
    case 7:
        console.log("Hoje é sábado.");
        break;
    default:
        console.log("Que dia é hoje?");
        break
}

let animal = "Leão";

switch (animal) {
    case "Leão":
        console.log("Este animal é vertebrado.");
        break;
    case "Polvo":
        console.log("Este animal é invertebrado.");
        break;
    case "Urso":
        console.log("Este animal é onívoro.");
        break;
    case "Galinha":
        console.log("Este animal é ovíparo.");
        break;
    case "Vaca":
        console.log("Este animal é herbívoro.");
        break;
    default:
        console.log("Animal não identificado.");
        break;
}