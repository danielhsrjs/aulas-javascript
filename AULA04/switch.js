let nota = "C" //A = Ótimo - B = Regular - C = Ruim

switch (nota) {
    case "A": {
        console.log("Sua nota é ótima!");
        break;
    }
    case "B": {
        console.log("Sua nota é regular.");
        break;
    }
    case "C": {
        console.log("Sua nota é ruim.")
        break;
    }
    default: {
        console.log("Nota não atribuida.");
        break;
    }
}